# Create a Program to Calculate Areas

# Area of Square
def square(side):
    area = side * side
    print("The area of the square is " + str(area) + " square units.")

# Area of Circle
def circle(radius):
    pi = 3.14
    area = pi * radius * radius
    print("The area of the circle is " + str(area) + " square units.")

# Test functions
square(8)
circle(7)
